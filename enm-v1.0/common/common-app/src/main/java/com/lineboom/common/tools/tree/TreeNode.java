package com.lineboom.common.tools.tree;

public class TreeNode {
	private Object id;
	private Object text;
	private Object cls;
	public Object getId() {
		return id;
	}
	public void setId(Object id) {
		this.id = id;
	}
	public Object getText() {
		return text;
	}
	public void setText(Object text) {
		this.text = text;
	}
	public Object getCls() {
		return cls;
	}
	public void setCls(Object cls) {
		this.cls = cls;
	}
}
